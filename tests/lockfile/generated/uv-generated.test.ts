import { describe, it } from "vitest";
import {
  GENERATED_LOCKFILE_TEST_ENABLED,
  expectResolvableVersion,
  runCommand,
  withTempProject,
} from "./helpers";

const describeGenerated = GENERATED_LOCKFILE_TEST_ENABLED
  ? describe
  : describe.skip;

describeGenerated("generated uv.lock", () => {
  it("parses uv.lock generated by uv add --dev tombi", async () => {
    await withTempProject("setup-tombi-uv-", async (projectDir) => {
      runCommand("uv init --no-readme", projectDir);
      runCommand("uv add --dev tombi", projectDir);
      await expectResolvableVersion(projectDir, "uv.lock");
    });
  }, 180_000);
});
