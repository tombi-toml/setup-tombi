import { describe, it } from "vitest";
import {
  GENERATED_LOCKFILE_TEST_ENABLED,
  expectResolvableVersion,
  runCommand,
  withTempProject,
} from "./helpers";

const describeGenerated = GENERATED_LOCKFILE_TEST_ENABLED
  ? describe
  : describe.skip;

describeGenerated("generated package-lock.json", () => {
  it("parses package-lock.json generated by npm install --save-dev @tombi-toml/tombi", async () => {
    await withTempProject("setup-tombi-npm-", async (projectDir) => {
      runCommand("npm init -y", projectDir);
      runCommand("npm install --save-dev @tombi-toml/tombi", projectDir);
      await expectResolvableVersion(projectDir, "package-lock.json");
    });
  }, 180_000);
});
